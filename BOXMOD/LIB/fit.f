C
      SUBROUTINE FIT (T,TE,DT)
C
C **********************************************************************
C PROGRAMMES APPELANTS : TWOSTEP, NEWDT 
C PROGRAMMES APPELES   : NONE
C **********************************************************************
C
C
C **********************************************************************
C ETAPE 1 : DECLARATION
C **********************************************************************
C
      IMPLICIT NONE
C
C ----------------------------------------------------------------------
C 1.1 VARIABLES D INTERFACE
C ---------------------------------------------------------------------
C
C TE     R    INPUT
C T      R    INPUT
C DT     R    INPUT/OUTPUT
C
      REAL TE, T, DT
C
C --------------------------------------------------------------------
C 1.2 VARIABLES LOCALES
C -------------------------------------------------------------------
C
      INTEGER NS
      REAL RNS
C
C **********************************************************************
C ETAPE 2 :
C *********************************************************************
C
      RNS=(TE-T)/DT
C
      IF (RNS.GT.10.0) GOTO 10
C
      NS=INT(RNS)+1
      DT=(TE-T)/NS
      DT=(DT+T)-T
C
   10 RETURN
C
      END
